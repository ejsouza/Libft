#include "../includes/libft.h"

int			ft_int_len(int nb)
{
	int		i;

	i = 0;
	if (nb <= i)
		i = (i < 0) ? 2 : 1;
	while (nb)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
