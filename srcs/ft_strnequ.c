#include "../includes/libft.h"

int				ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int			i;

	if (s1 && s2)
	{
		i = 0;
		while ((n && s1[i] != '\0') || (n && s2[i] != '\0'))
		{
			if (s1[i] != s2[i])
				return (0);
			i++;
			n--;
		}
	}
	return (1);
}
