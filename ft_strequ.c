#include "libft.h"

int			ft_strequ(char const *s1, char const *s2)
{
	int		i;

	if (s1 && s2)
	{
		i = ft_strcmp((char *)s1, (char *)s2);
		if (i)
			return (0);
		else
			return (1);
	}
	return (1);
}
